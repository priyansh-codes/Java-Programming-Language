package assignment;

public class AssignmentOperator {
    public static void main(String[] args) {

        //Used to assign a value to variable

        int a = 10;

        a+=10;  //Other form of writing a = a + 10 , faster execution
        System.out.println(a);
        a-=10;  //a = a = 10 , returns 20-10=10
        System.out.println(a);
        a*=2;   // a = a * 2 , returns 10*2 = 20
        System.out.println(a);
        a/=5;   //a = a/5 , returns 4
        System.out.println(a);

    }
}
